package ro.cloudSoft.cloudDoc.presentation.client.shared.model.arb;

public class MembruReprezentantiComisieSauGLInfoModel {
	
	private String nume;
	private String prenume;
	private String functie;
	private String departament;
	private String email;
	private String telefon;
	private String calitate;
	
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public String getPrenume() {
		return prenume;
	}
	public void setPrenume(String prenume) {
		this.prenume = prenume;
	}
	public String getFunctie() {
		return functie;
	}
	public void setFunctie(String functie) {
		this.functie = functie;
	}
	public String getDepartament() {
		return departament;
	}
	public void setDepartament(String departament) {
		this.departament = departament;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTelefon() {
		return telefon;
	}
	public void setTelefon(String telefon) {
		this.telefon = telefon;
	}
	public String getCalitate() {
		return calitate;
	}
	public void setCalitate(String calitate) {
		this.calitate = calitate;
	}
}
