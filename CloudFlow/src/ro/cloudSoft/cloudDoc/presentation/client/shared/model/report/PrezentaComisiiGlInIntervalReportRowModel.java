package ro.cloudSoft.cloudDoc.presentation.client.shared.model.report;

public class PrezentaComisiiGlInIntervalReportRowModel {

	private String institutie;
	private String prticipant;
	private String functie;
	private String departament;
	private String email;
	private String telefon;

	public String getInstitutie() {
		return institutie;
	}

	public void setInstitutie(String institutie) {
		this.institutie = institutie;
	}

	public String getPrticipant() {
		return prticipant;
	}

	public void setPrticipant(String prticipant) {
		this.prticipant = prticipant;
	}

	public String getFunctie() {
		return functie;
	}

	public void setFunctie(String functie) {
		this.functie = functie;
	}

	public String getDepartament() {
		return departament;
	}

	public void setDepartament(String departament) {
		this.departament = departament;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTelefon() {
		return telefon;
	}

	public void setTelefon(String telefon) {
		this.telefon = telefon;
	}

}
